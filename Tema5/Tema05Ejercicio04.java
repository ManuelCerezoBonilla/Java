/**
 *
 * Muestra los números del 320 al 160, contando de 20 en 20 hacia atrás
 * utilizando un bucle for .
 *
 * @author Manuel Cerezo
 *
 */

public class Tema05Ejercicio04 {
  public static void main(String[] args) {

    for(int i = 320; i >= 160; i-=20) {
      System.out.println(i);//Lo mismo que con el ejercicio 1, mostramos los números que queremos (especificado en el enunciado), mostrando 1 y saltando de línea continuamente.
    }

  }
}
